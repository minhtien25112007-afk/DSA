def my_sqrt(n):
    left = 0
    right = n
    ans = 0

    while left <= right:
        mid = (left + right) // 2

        if mid * mid <= n:
            ans = mid
            left = mid + 1
        else:
            right = mid - 1

    return ans

print(my_sqrt(8))
print(my_sqrt(16))